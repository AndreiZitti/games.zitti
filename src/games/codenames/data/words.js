/**
 * Get random words for a new game
 * @param {string} language - 'en' or 'ro'
 * @param {number} count - Number of words (default 25)
 * @returns {string[]} Array of random words
 */
export function getRandomWords(language = 'en', count = 25) {
  const wordList = language === 'ro' ? wordsRO : wordsEN;

  // Fisher-Yates shuffle
  const shuffled = [...wordList];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled.slice(0, count);
}

/**
 * Generate a key card for the game board
 * Determines which positions are red/blue/neutral/assassin
 * @returns {Object} Key card with position assignments
 */
export function generateKeyCard() {
  // Generate positions 0-24
  const positions = Array.from({ length: 25 }, (_, i) => i);

  // Fisher-Yates shuffle
  for (let i = positions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [positions[i], positions[j]] = [positions[j], positions[i]];
  }

  // First team gets 9, second gets 8
  const firstTeam = Math.random() < 0.5 ? 'red' : 'blue';
  const secondTeam = firstTeam === 'red' ? 'blue' : 'red';

  return {
    [firstTeam]: positions.slice(0, 9),
    [secondTeam]: positions.slice(9, 17),
    neutral: positions.slice(17, 24),
    assassin: positions[24],
    firstTeam
  };
}

/**
 * Get the card type for a given position
 * @param {Object} keyCard - The key card object
 * @param {number} position - Board position (0-24)
 * @returns {string} 'red' | 'blue' | 'neutral' | 'assassin'
 */
export function getCardType(keyCard, position) {
  if (keyCard.red.includes(position)) return 'red';
  if (keyCard.blue.includes(position)) return 'blue';
  if (keyCard.neutral.includes(position)) return 'neutral';
  if (keyCard.assassin === position) return 'assassin';
  return 'neutral'; // fallback
}
